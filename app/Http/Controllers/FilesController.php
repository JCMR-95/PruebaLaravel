    public function upload(Request $request)
    {
        if($request->hasFile("url")){

            $file = $request->file("url");
            $name = time().".".$file->guessExtension();
            $route = public_path("storage/".$name);

            copy($file, $route);

        }
        return view('filesViews.uploadFile');
    }

